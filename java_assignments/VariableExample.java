class VariableExample1 
{
	public static void main(String[] args) 
	{
		//variable declaration
			int a;
			double d;
			char ch;
			boolean b;


		//variable initialisation
		    a=100;
			d=100.2;
			ch='B';
			b=false;
		  
		System.out.println("a="+a);
		System.out.println("d="+d);
		System.out.println("ch="+ch);
		System.out.println("b="+b);
	}
}
